package com.loginext.website.components.home;

public class WhyLoginextCardData {
	private String link;
	private String title;
	private String description;

	public WhyLoginextCardData(String link, String title, String description) {
		this.link = link;
		this.title = title;
		this.description = description;
	}

	public String getLink() {
		return link;
	}

	public String getTitle() {
		return title;
	}

	public String getDescription() {
		return description;
	}
}
